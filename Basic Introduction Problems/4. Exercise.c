
/*Program that will declare an integer, a floating point number, a character. Then it will
initialize them with values and print those values.
Sample input Sample output
The integer value: 5
The floating point value: 3.141593
The character value: a
The integer value: 100
The floating point value: 1.618000
The character value:*/
 #include<stdio.h>
 int main()
 {
     int A;
     float B;
     char C;
     A=5;
     B=3.1416;
     C='a';

     printf("The integer value:%d\n",A);
     printf("The floating point value:%f\n",B);
     printf("The character value:%c\n",C);
     getchar();
 }
